let date1 = new Date();

// Current year
console.log(date1.getFullYear());

// Month (0 = Jan, 11 = Dec)
console.log(date1.getMonth());

// Day of the month
console.log(date1.getDate());

// Day of the week (0 = Sunday)
console.log(date1.getDay());

// Current time (hours, minutes, seconds)
console.log(
    date1.getHours(),
    date1.getMinutes(),
    date1.getSeconds()
);
